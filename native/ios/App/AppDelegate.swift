extension AppDelegate: CXProviderDelegate {

    func providerDidReset(_ provider: CXProvider) {
        print("üìû [CallKit] Provider reset")
    }

    func provider(_ provider: CXProvider, perform action: CXStartCallAction) {
        // Outgoing call
        print("üìû [CallKit] Start call action")

        // Configure audio session for call
        _ = GhostVoiceEngine.shared.configureAudioSession()

        action.fulfill()
    }

    func provider(_ provider: CXProvider, perform action: CXAnswerCallAction) {
        // Incoming call answered
        print("üìû [CallKit] Answer call action")

        _ = GhostVoiceEngine.shared.configureAudioSession()
        GhostVoiceEngine.shared.startVoiceSession()

        action.fulfill()
    }

    func provider(_ provider: CXProvider, perform action: CXEndCallAction) {
        // Call ended
        print("üìû [CallKit] End call action")

        GhostVoiceEngine.shared.stopVoiceSession()

        action.fulfill()
    }

    func provider(_ provider: CXProvider, perform action: CXSetMutedCallAction) {
        // Mute toggled
        print("üìû [CallKit] Mute action: \(action.isMuted)")

        GhostVoiceEngine.shared.setMuted(action.isMuted)

        action.fulfill()
    }

    func provider(_ provider: CXProvider, didActivate audioSession: AVAudioSession) {
        print("üìû [CallKit] Audio session activated")
    }

    func provider(_ provider: CXProvider, didDeactivate audioSession: AVAudioSession) {
        print("üìû [CallKit] Audio session deactivated")
    }
}

